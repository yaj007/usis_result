def generate_url(student_id, student_name):
    base_url = "https://usis.bracu.ac.bd/academia/docuJasper/index"
    params = {
        "studentId": student_id,
        "reportFormat": "PDF",
        "old_id_no": student_id,
        "strMessage": "",
        "scholarProgramMsg": "",
        "companyLogo": "/var/academia/image/universityLogo/1571986355.jpg",
        "companyName": "BRAC+University",
        "headerTitle": "GRADE+SHEET",
        "companyAddress": "66,+MOHAKHALI+C/A,+DHAKA+-+1212.",
        "academicStanding": "Satisfactory",
        "gradeSheetBackground": "/bits/usis/tomcat/webapps/academia/images/gradeSheetBackground.jpg",
        "_format": "PDF",
        "_name": f"GRADE_SHEET_{student_id}_{student_name.replace(' ', '_')}",
        "_file": "student/rptStudentGradeSheetForStudent.jasper"
    }

    # Construct the updated URL
    updated_url = base_url + "?" + "&".join([f"{key}={value}" for key, value in params.items()])

    return updated_url

# Input values
student_id = input('Enter Target ID:')
student_name = input('Enter Target Name:')

# Generate and print the updated URL
updated_url = generate_url(student_id, student_name)
